package ua.javarush.task.pro.task13.task1313;

/* 
StringLinkedList
Настав час познайомитися із двобічно зв'язаним списком. Розв'язуючи цю задачу, ти зрозумієш, як працює двобічно
зв'язаний список, і зможеш додавати в нього елементи. У тебе є клас StringLinkedList, який має поля first і last,
що вказують, відповідно, на перший та останній елементи.
Тобі потрібно реалізувати логіку методу add, який буде додавати елементи до списку.

Підказки

Для правильної реалізації слід розуміти, що списком можна пересуватися як від першого елемента до останнього, так і в
зворотному напрямку. Тому, додаючи нові елементи, не забувай змінювати посилання в сусідніх елементах.
Метод add має створювати новий об'єкт класу Node, куди буде записано передане значення. Перш ніж додавати новий вузол,
потрібно змінити посилання останнього вузла, вказавши новий вузол, а новий вузол стане останнім.
Якщо список містить один елемент, то на нього вказують одночасно і first.next, і last.prev.
Метод printAll створено для налагодження (debug), і в перевірці він не використовується.
Важливо!
Поле Node first не повинно містити значення — воно тільки вказує на перший елемент(first.next);
Node last також не містить значення, а тільки вказує на останній елемент(last.prev).


Requirements:
1. Під час додавання нового елемента попередній елемент має отримувати посилання на нього.
2. Під час додавання нового елемента він має отримувати посилання на попередній.
3. Поле first класу StringLinkedList має завжди посилатися на перший елемент, не повинно містити значення (value) і
посилання на попередній елемент (prev).
4. Поле last класу StringLinkedList має завжди посилатися на останній елемент, не повинно містити значення (value) і
посилання на наступний елемент (next).
*/

public class Solution {

    public static void main(String[] args) {
        StringLinkedList stringLinkedList = new StringLinkedList();
        stringLinkedList.add("1");
        stringLinkedList.add("2");
        stringLinkedList.add("3");
        stringLinkedList.add("4");
        stringLinkedList.add("5");
        stringLinkedList.add("6");
        stringLinkedList.add("7");
        stringLinkedList.add("8");
        stringLinkedList.add("9");
        stringLinkedList.printAll();
    }
}
