Напиши запит, який з таблиці cars вибере назву (name) та кількість автомобілів із певною назвою.
До вибірки повинні потрапити лише автомобілі з кількістю менше 3.
Для кількості використовуй аліас car_count, і цей аліас використовуй для перевірки умови.

Requirements:
1. Запит має бути реалізований згідно з умовою.
